
public class pair {

	private float value;
	private String s;

	public pair() {
		s = "";
		value = 0;
	}

	public float getValue() {
		return value;
	}

	public String getName() {
		return s;
	}

	public void setValue(float value) {
		this.value = value;
	}

	public void setName(String s) {
		this.s = s;
	}

}
